#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*s2;
	int		len;

	i = 0;
	len = ft_strlen(s);
	s2 = (char *)malloc (sizeof(char) * (len + 1));
	if (!s2)
		return (NULL);
	while (s[i] != '\0')
	{
		s2[i] = s[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
